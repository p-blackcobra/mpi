.data
.code
MAIN PROC
MOV AL,90
MOV DX,3006
OUT DX, AL
LOOP1: MOV DX,3000 
IN AL,DX
NOT AL 
MOV DX,3002
OUT DX, AL
MOV AL,02 
MOV DX,3006
OUT DX, AL
CALL DELAY
MOV AL,03
MOV DX,3006
OUT DX, AL
CALL DELAY
MOV AL,0AH
MOV DX,3006
OUT DX, AL
CALL DELAY
MOV AL,0BH
MOV DX,3006
OUT DX, AL
CALL DELAY
MOV AL,0EH
MOV DX,3006
OUT DX, AL
CALL DELAY
MOV AL,0FH
MOV DX,3006
OUT  DX, AL
CALL DELAY
JMP LOOP1

DELAY:
MOV CX,7FFFH
NEXT: LOOP NEXT
RET

MAIN ENDP
END